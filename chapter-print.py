# 转义字符
print('my\nlover') # \n，newline，另起一行
print('my\tlover') # \t，TAB,制表位，表示四个制表位的，未填满，剩余部分用空格表示
print('my\blover') # \b，back，回退一格
print('my\rlover') # \r，return，回车，后者覆盖前面的部分

# 原字符
print('http:\\\\baidu.com') # 为了打出两个\\,因为两个\\转义字符表示为一个\
print('老师说：\"\'大家上午好\"\'') # 将转义字符无转义输出’”\，在其左侧加个\

