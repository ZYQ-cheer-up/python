# 测试对象的布尔值
# bool值为0的对象：False
print(bool(False))
# 数值0
print(bool(0))
print(bool(0.0))
# 空字符串
print(bool(None))
print(bool(''))
print(bool(""))
# 空列表
print(bool([]))
print(bool(list()))
# 空元组
print(bool(()))
print(bool(tuple()))
# 空字典
print(bool({}))
print(bool(dict()))
# 空集合
print(bool(set()))
# 除此之外的其他对象的布尔值都为Ture
